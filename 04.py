"""
Faça um programa que pede dois inteiros e armazene em duas variáveis. Em seguida, 
troque o valor das variáveis e exiba na tela
"""

n1 = int(input("Digite um numero inteiro: "))
n2 = int(input("Digite outro numero inteiro: "))

primeiro = n2
segundo = n1

print(f"Primeiro numero: {n1}")
print(f"Segundo numero: {n2}")

print("Trocando valores...")

print(f"Primeiro numero:{primeiro}")
print(f"Segundo numero:{segundo}")